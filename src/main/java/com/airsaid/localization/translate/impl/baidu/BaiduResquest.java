package com.airsaid.localization.translate.impl.baidu;

public class BaiduResquest {

    private String q;
    private String from;
    private String to;
    private String termIds;

    public String getQ() {
        return q;
    }

    public void setQ(String q) {
        this.q = q;
    }

    public String getFrom() {
        return from;
    }

    public void setFrom(String from) {
        this.from = from;
    }

    public String getTo() {
        return to;
    }

    public void setTo(String to) {
        this.to = to;
    }

    public String getTermIds() {
        return termIds;
    }

    public void setTermIds(String termIds) {
        this.termIds = termIds;
    }
}
